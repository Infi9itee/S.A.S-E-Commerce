    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       // Get the new values from the text fields
        String newName = jTextField3.getText();
        String newEmail = jTextField4.getText();
        String newPassword = jTextField5.getText();

        String currentUsername = "YOUR_CURRENT_USERNAME"; // Replace with the actual current username.

        // Read the existing data from the file
        try {
            File file = new File(FILE_PATH);
            BufferedReader br = new BufferedReader(new FileReader(file));
            String line;
            StringBuilder fileContent = new StringBuilder();

            // Read each line from the file
            while ((line = br.readLine()) != null) {
                String[] parts = line.split(": ");
                if (parts.length == 2) {
                    String fieldName = parts[0];
                    String fieldValue = parts[1].trim();

                    if ("Full Name".equals(fieldName) && currentUsername.equals(fieldValue)) {
                        line = "Full Name: " + newName;
                    } else if ("Email".equals(fieldName) && currentUsername.equals(fieldValue)) {
                        line = "Email: " + newEmail;
                    } else if ("Password".equals(fieldName) && currentUsername.equals(fieldValue)) {
                        line = "Password: " + newPassword;
                    }
                }
                // Append the line to the file content
                fileContent.append(line).append("\n");
            }
            br.close();

            // Write the updated file content back to the file
            BufferedWriter bw = new BufferedWriter(new FileWriter(file));
            bw.write(fileContent.toString());
            bw.close();

            JOptionPane.showMessageDialog(this, "Profile updated successfully.");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error updating profile: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    
